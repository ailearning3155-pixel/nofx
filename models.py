class NewsSentiment(Base):
    """Cached news sentiment scores per instrument."""
    __tablename__ = "news_sentiment"

    id = Column(Integer, primary_key=True)
    instrument = Column(String(20), nullable=False)
    headline = Column(String(500), nullable=False)
    source = Column(String(100), nullable=True)
    url = Column(String(500), nullable=True)

    sentiment_score = Column(Float, nullable=True)  # -1.0 to 1.0
    sentiment_label = Column(String(20), nullable=True)  # positive|negative|neutral
    model_used = Column(String(50), nullable=True)

    published_at = Column(DateTime, nullable=True)
    fetched_at = Column(DateTime, default=datetime.utcnow)

    __table_args__ = (
        Index("ix_news_instrument", "instrument"),
        Index("ix_news_fetched", "fetched_at"),
    )


class EconomicEvent(Base):
    """Economic calendar events."""
    __tablename__ = "economic_events"

    id = Column(Integer, primary_key=True)
    title = Column(String(255), nullable=False)
    currency = Column(String(10), nullable=False)
    impact = Column(Enum(ImpactLevel), nullable=False)

    event_time = Column(DateTime, nullable=False)
    forecast = Column(String(50), nullable=True)
    previous = Column(String(50), nullable=True)
    actual = Column(String(50), nullable=True)

    trading_paused = Column(Boolean, default=False)  # did we pause for this?
    fetched_at = Column(DateTime, default=datetime.utcnow)

    __table_args__ = (
        Index("ix_events_time", "event_time"),
        Index("ix_events_currency", "currency"),
    )


