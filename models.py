class BacktestRun(Base):
    """A completed backtest run."""
    __tablename__ = "backtest_runs"

    id = Column(Integer, primary_key=True)
    name = Column(String(255), nullable=False)
    instrument = Column(String(20), nullable=False)
    granularity = Column(String(10), nullable=False)
    strategy = Column(String(100), nullable=True)
    ai_model = Column(String(50), nullable=True)

    start_date = Column(DateTime, nullable=False)
    end_date = Column(DateTime, nullable=False)
    initial_balance = Column(Float, default=10000.0)

    # Results
    final_balance = Column(Float, nullable=True)
    total_return_pct = Column(Float, nullable=True)
    total_trades = Column(Integer, nullable=True)
    win_rate = Column(Float, nullable=True)
    max_drawdown = Column(Float, nullable=True)
    sharpe_ratio = Column(Float, nullable=True)
    profit_factor = Column(Float, nullable=True)
    sortino_ratio = Column(Float, nullable=True)

    parameters = Column(JSON, default=dict)
    equity_curve = Column(JSON, default=list)   # list of {timestamp, equity}
    trade_log = Column(JSON, default=list)

    status = Column(String(20), default="running")  # running | completed | failed
    error_message = Column(Text, nullable=True)
    duration_seconds = Column(Float, nullable=True)
    created_at = Column(DateTime, default=datetime.utcnow)
    completed_at = Column(DateTime, nullable=True)
